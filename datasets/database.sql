CREATE TABLE IF NOT EXISTS `tbldatasets` (
  `dataset_id` int UNSIGNED NOT NULL PRIMARY KEY,
  `dataset_user` int UNSIGNED NOT NULL,
  `dataset_name` varchar NOT NULL,
  `dataset_language` int UNSIGNED NOT NULL,
  `dataset_subject` int UNSIGNED NOT NULL,
  `dataset_official` tinyint NOT NULL,
  `dataset_published` tinyint NOT NULL,
  `dataset_online_id` int UNSIGNED NOT NULL,
  `dataset_date` datetime NOT NULL,
  `dataset_lastedited` datetime NOT NULL
) ;

CREATE TABLE IF NOT EXISTS `tblitems` (
  `item_id` int UNSIGNED NOT NULL PRIMARY KEY,
  `item_dataset` int UNSIGNED NOT NULL,
  `item_online_id` int UNSIGNED NOT NULL,
  `item_question` text NOT NULL,
  `item_answer` text  NOT NULL,
  `item_hint` text  NOT NULL
);

CREATE TABLE IF NOT EXISTS `tbllanguages` (
  `language_id` int UNSIGNED NOT NULL PRIMARY KEY,
  `language_name` text NOT NULL,
  `language_short` text NOT NULL
) ;

CREATE TABLE IF NOT EXISTS `tblsubjects` (
  `subject_id` int NOT NULL,
  `subject_name` varchar NOT NULL
); 


CREATE TABLE IF NOT EXISTS `tblusers` (
  `user_id` int NOT NULL,
  `user_email` varchar NOT NULL,
  `user_firstname` varchar NOT NULL,
  `user_lastname` varchar NOT NULL,
  `user_password` int NOT NULL,
  `user_createdate` date NOT NULL,
  `user_modified_date` datetime NOT NULL,
  `user_language` int UNSIGNED NOT NULL
) ;


CREATE TABLE IF NOT EXISTS `tbluser_items` (
  `user_item_id` int UNSIGNED NOT NULL PRIMARY KEY,
  `user_item_user` int UNSIGNED NOT NULL ,
  `user_item_strength` REAL NOT NULL
) ;

